// —Ñ–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–∏–µ –≤—Ä–µ–º–µ–Ω–∏
function formatTime(totalSeconds) {
    if (totalSeconds <= 0) return "00:00:00";
    const h = String(Math.floor(totalSeconds / 3600)).padStart(2, "0");
    const m = String(Math.floor((totalSeconds % 3600) / 60)).padStart(2, "0");
    const s = String(totalSeconds % 60).padStart(2, "0");
    return `${h}:${m}:${s}`;
}

function updateTimer() {
    const now = Date.now();
    const diffMs = endTime - now;
    const remaining = Math.floor(diffMs / 1000);

    if (remaining <= 0) {
        clearInterval(timerId);
        timerId = null;
        timerDisplay.textContent = "–ú–æ–∂–Ω–æ —Ç—è–Ω—É—Ç—å –Ω–æ–≤—É—é –∫–∞—Ä—Ç—É";
        localStorage.removeItem(STORAGE_KEY_END_TIME);
        return;
    }

    timerDisplay.textContent = formatTime(remaining);
}

function startTimer() {
    if (!endTime) return;
    if (timerId) clearInterval(timerId);
    updateTimer();
    timerId = setInterval(updateTimer, 1000);
}

// –±–æ–∫–æ–≤—ã–µ –∫–∞—Ä—Ç–∏–Ω–∫–∏ + –ø–æ–¥—Å–≤–µ—Ç–∫–∞
function applySideImages(card) {
    if (sidebarLeft) {
        sidebarLeft.style.backgroundImage = `url("${card.sideLeft}")`;
        sidebarLeft.classList.add("sidebar-glow");
    }
    if (sidebarRight) {
        sidebarRight.style.backgroundImage = `url("${card.sideRight}")`;
        sidebarRight.classList.add("sidebar-glow");
    }
}

// —Å–ª—É—á–∞–π–Ω–∞—è –∫–∞—Ä—Ç–∞
function drawRandomCard() {
    const now = Date.now();
    // –µ—Å–ª–∏ —Ç–∞–π–º–µ—Ä –µ—â—ë –∏–¥—ë—Ç ‚Äî –Ω–µ –¥–∞—ë–º –≤—ã—Ç—è–Ω—É—Ç—å –Ω–æ–≤—É—é –∫–∞—Ä—Ç—É
    if (endTime && endTime > now) {
        return;
    }

    const randomIndex = Math.floor(Math.random() * cards.length);
    const card = cards[randomIndex];

    cardTitleElement.textContent = card.title;
    cardTextElement.textContent = card.text;
    applySideImages(card);

    localStorage.setItem(STORAGE_KEY_CARD_KEY, card.key);

    endTime = Date.now() + TIMER_SECONDS * 1000;
    localStorage.setItem(STORAGE_KEY_END_TIME, String(endTime));
    startTimer();
}

function loadSavedState() {
    const savedEndTime = localStorage.getItem(STORAGE_KEY_END_TIME);
    const savedKey = localStorage.getItem(STORAGE_KEY_CARD_KEY);

    if (savedEndTime) {
        endTime = Number(savedEndTime);
        const now = Date.now();

        if (endTime > now && savedKey) {
            const card = cards.find(c => c.key === savedKey);
            if (card) {
                cardTitleElement.textContent = card.title;
                cardTextElement.textContent = card.text;
                applySideImages(card);
                dailyCardElement.classList.add("flipped");
                startTimer();
                return;
            }
        } else {
            // —Ç–∞–π–º–µ—Ä –ø—Ä–æ—à—ë–ª ‚Äî –æ—á–∏—â–∞–µ–º —Å–æ—Å—Ç–æ—è–Ω–∏–µ
            endTime = null;
            localStorage.removeItem(STORAGE_KEY_END_TIME);
            localStorage.removeItem(STORAGE_KEY_CARD_KEY);
        }
    }

    timerDisplay.textContent = "–ù–∞–∂–º–∏ –Ω–∞ –∫–∞—Ä—Ç—É";
}

window.addEventListener("DOMContentLoaded", loadSavedState);

